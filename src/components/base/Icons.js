        // Volleyball
        volleyball: `<svg xmlns="http://www.w3.org/2000/svg" width="${size}" height="${size}" viewBox="0 0 24 24" fill="none" stroke="${color}" stroke-width="${strokeWidth}" stroke-linecap="round" stroke-linejoin="round" class="${className}"><circle cx="12" cy="12" r="10"></circle><path d="M12 2 C 16 6, 16 18, 12 22"></path><path d="M12 2 C 8 6, 8 18, 12 22"></path><path d="M2 12 C 6 8, 18 8, 22 12"></path><path d="M2 12 C 6 16, 18 16, 22 12"></path></svg>`,

